const fruits = ["Apple", "Banana"];

// Add to end
fruits.push("Orange");
console.log("After push:", fruits);

// Remove from end
fruits.pop();
console.log("After pop:", fruits);

// Add to beginning
fruits.unshift("Mango");
console.log("After unshift:", fruits);

// Remove from beginning
fruits.shift();
console.log("After shift:", fruits);
